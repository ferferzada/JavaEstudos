package lista03.ex04.share;

public class Point {

    private final double axleX;

    private final double axleY;

    public Point(double x, double y){
        this.axleX = x;
        this.axleY = y;
    }

    public double getAxleX() {
        return axleX;
    }

    public double getAxleY() {
        return axleY;
    }
}
